# Numeric variables example
x = 1
y = 1.234
z = True

print(x)
print(y)
print(z)

# Arithmetic operations
x = 3
y = 8

sum = x + y
print(sum)

# User input
x = int(input("Enter x: "))
y = int(input("Enter y: "))

sum = x + y
print(sum)

# Python List
list = ["Drake", "Derp", "Derek", "Dominique"]

print(list)         # prints all elements
print(list[0])      # prints first element
print(list[1])      # prints second element

# Add/remove
print(list)
list.append("Victoria")
print(list)
list.remove("Derp")
list.remove("Drake")
print(list)

# Sort list
list.sort()         # sorts list in alphabetical order
print(list)

# Reverse list order
list.reverse()      # reverse the order of list
print(list)
