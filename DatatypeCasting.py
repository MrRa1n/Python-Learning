# Python determines the datatype automatically

x = 3           # Integer
y = "text"      # String

x = 4
y = 2.15315224522

print("We have defined two numbers, ")
print("x = " + str(x))      # Datatype cast to String using str()
print("y = " + str(y))      # Datatype cast to String using str()

a = "135.24552"
b = "133.255632"

c = float(a) + float(b)
print(c)

